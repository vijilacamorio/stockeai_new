            return;
        }
        try {
            const canvases = await Promise.all(elements.map(async (element) => {
                // Temporary spans to hold input values
                const inputs = element.querySelectorAll('input');
                const spans = [];
                inputs.forEach(input => {
                    const span = document.createElement('span');
                    span.style.cssText = window.getComputedStyle(input).cssText;
                    span.style.border = 'none';
                    span.textContent = input.value;
                    input.parentNode.insertBefore(span, input);
                    spans.push(span);
                    input.style.display = 'none';
                });
                // Render the canvas with input values
                const canvas = await html2canvas(element, { scale: 2 });
                // Restore the original state
                spans.forEach(span => span.remove());
                inputs.forEach(input => input.style.display = '');
                return canvas;
            }));
            const pdf = new jspdf.jsPDF({
                orientation: 'p',
                unit: 'px',
                format: [canvases[0].width, canvases[0].height]
            });
