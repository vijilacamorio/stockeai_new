                const imgData = canvas.toDataURL('image/jpeg', 1.0);
                if (index > 0) {
                    pdf.addPage([canvas.width, canvas.height], 'p');
                }
                pdf.addImage(imgData, 'JPEG', 0, 0, canvas.width, canvas.height);
            });
